UPDATE "YELP"."SENTIMENT_TABLE_TRAINING" SET WN = 0 WHERE WN IS NULL;
UPDATE "YELP"."SENTIMENT_TABLE_TRAINING" SET SN = 0 WHERE SN IS NULL;


